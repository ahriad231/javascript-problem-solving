function budgetCalculator(watch,mobile,laptop){
    var watchPrice=watch*50;
    var mobilePrice=mobile*100;
    var laptopPrice=laptop*500;
    var totalPrice=(watchPrice+mobilePrice+laptopPrice);
    return totalPrice;
}
